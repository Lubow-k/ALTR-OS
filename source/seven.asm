_change_esp:
    mov esp, [esp + 4]
    jmp _recovery_ctx

_recovery_ctx:     ; in timer
    mov dx, 0x20
    mov al, 0x20
    out dx, al
    pop esp
    popa
    pop gs
    pop fs
    pop es
    pop ds
    add esp, 8
    IRETD


collect_context:
    push ds
    push es
    push fs
    push gs
    pusha
    push esp

    ; ds and es
    mov eax, 0x10  ; aka 16 (data segment)
    mov ds, eax
    mov es, eax

    call _interrupt_handler
    jmp _recovery_ctx

